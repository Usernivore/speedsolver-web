    p: number;    // Pressure in kPa
    t: number;    // Temperature in °C
    vf: number;   // Sp. volume sat. liquid (m³/kg)
    vg: number;   // Sp. volume sat. vapor (m³/kg)
    hf: number;   // Enthalpy sat. liquid (kJ/kg)
    hg: number;   // Enthalpy sat. vapor (kJ/kg)
    sf: number;   // Entropy sat. liquid (kJ/kg·K)
